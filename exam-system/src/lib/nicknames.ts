export type NicknameGenre = keyof typeof nicknameCandidatesByGenre;

export const nicknameGenres = Object.keys(nicknameCandidatesByGenre) as NicknameGenre[];

export const NICKNAME_REFRESH_LIMIT = 3;

export function createNicknameNumber() {
  return String(Math.floor(Math.random() * 1000)).padStart(3, "0");
}

export function createNicknameOptions(genre: NicknameGenre, count = 6) {
  const source = nicknameCandidatesByGenre[genre];
  const shuffled = [...source];

  for (let index = shuffled.length - 1; index > 0; index -= 1) {
    const swapIndex = Math.floor(Math.random() * (index + 1));
    [shuffled[index], shuffled[swapIndex]] = [shuffled[swapIndex], shuffled[index]];
  }

  return shuffled.slice(0, Math.min(count, shuffled.length));
}

export function buildNickname(candidate: string, number: string) {
  return `${candidate}${number}`;
}

export function isGeneratedNickname(value: string) {
  const nickname = value.trim();
  if (!nickname || nickname.length > 40) return false;

  return Object.values(nicknameCandidatesByGenre).some((candidates) =>
    candidates.some((candidate) => {
      if (!nickname.startsWith(candidate)) return false;
      const tail = nickname.slice(candidate.length);
      return /^\d{3}$/.test(tail);
    }),
  );
}

export function publicStudentName(student: { id: string; nickname?: string | null }) {
  const nickname = (student.nickname ?? "").trim();
  return nickname || "ニックネーム未設定";
}
